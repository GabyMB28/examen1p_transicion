import 'package:flutter/cupertino.dart';

class Marca{
  final int id;
  final String name;
  final String descripcion;
  final String precio;
  final String contacto;
  final String marca;

  Marca({
    required this.id,
    required this.name,
    required this.descripcion,
    required this.precio,
    required this.contacto,
    required this.marca,
  });
}

